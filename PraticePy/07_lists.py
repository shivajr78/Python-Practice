# Create list using []
a = [9,8,1,3,6,8,2,4,7,8]
# Print the list
print(a)

#Access using index using a[0],a[1],a[2] and so on........
print(a[9])

# Change the value of index using this
a[1] = 9
print(a)

# We can create lists with different items

a = ["shiva",8,2,4,7,8.1,True]
print(a)

#Slicing list
friends = ["Harry","Vinay","Soniya","Tanya",24]
print(friends[0:4])
print(friends[:-4])